#include "../../include/cub3D.h"

void	parse_map(t_game *game, char *line)
{
	static int	i = 0;

	if (line[0] == '1' || line[0] == ' ')
	{
		game->map->map[i] = ft_strdup(line);
		i++;
	}
}
