#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	if (d < s)
		ft_memcpy(dst, src, n);
	else if (d > s)
		while (n-- > 0)
			d[n] = s[n];
	return (dst);
}
